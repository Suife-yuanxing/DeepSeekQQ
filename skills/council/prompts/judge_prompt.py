# ── 质量门控逻辑 ──
def evaluate_gate(high_count: int, medium_count: int) -> str:
    """根据高/中严重性问题数量判定质量门控结果。

    Args:
        high_count: 🔴 高严重性问题数量（排除已解决的）
        medium_count: 🟡 中严重性问题数量

    Returns:
        "PASS" | "BLOCK" | "REVISE"
    """
    if high_count > 0:
        return "BLOCK"
    if medium_count > 3:
        return "REVISE"
    return "PASS"
